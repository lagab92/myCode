package BasicJava;

public class ComparisonOperator {

    // equal to ==
    public static void main(String[] args) {
        int p=7;
        int x=4;
        System.out.println(p==x);


        // not equal to !=
        System.out.println(p!=x);
        // greater than >
        System.out.println(x>p);
        // greater than or equal to >=
        System.out.println(x>=p);
        // less than <
        System.out.println(x<p);
        // less than or equal to <=
        System.out.println(x<=p);





    }


}
